// Método concat: Concatena um ou mais arrays retornando um novo array
const doces = ['brigadeiro', 'bolo','beijeinho','gelatina','bala de coco']
const bebidas = ['suco','refrigerante']
const salgados = ['coxinha','risole','pastelzinho','quibe']
const festAniver = doces.concat(bebidas,salgados)

console.log('Merge dos array atraves da função concat',festAniver)